                            {
                                Console.WriteLine("'Too short on Geo, little friend!' ");
                            }
                        }

                    }
                }

                if (room_counter == 20)
                {
                    
                    Console.WriteLine("The God Tamer leaps onto the stage!");

                    // Display both sprites
                    Console.WriteLine("THE RIDER:");
                    Console.WriteLine(Sprite_selecter(5));
                    Console.WriteLine("THE BEAST:");
                    Console.WriteLine(Sprite_selecter(6));

                    // Boss Setup
                    enemies[0] = "Godobble (Beast)";
                    enemies[1] = "God Tamer (Rider)";
                    combat_hp[0] = 200; // Beast is the tank
                    combat_hp[1] = 120; // Rider is faster but squishier

                    int beastDmg = 20;
                    int riderDmg = 15;
                    win = false;

                    while (win == false)
                    {
                        // --- 1. PLAYER TURNS ---
                        for (p = 0; p < 2; p++)
                        {
                            if (party_hp[p] <= 0)
                            {
                                continue;
                            }

                            Console.WriteLine("\n" + playerNames[p] + " HP: " + party_hp[p] + " | " + resourceNames[p] + ": " + meter[p]);
                            Console.WriteLine("Targets: 1. " + enemies[0] + " (" + combat_hp[0] + " HP) | 2. " + enemies[1] + " (" + combat_hp[1] + " HP)");
                            Console.WriteLine("Action: attack / spell / focus / block");
                            choice = Console.ReadLine().ToLower();

                            if (choice == "attack")
                            {
                                Console.WriteLine("Target 1 or 2?");
                                choice_attack = Convert.ToInt32(Console.ReadLine());
                                targetIndex = choice_attack - 1;

                                double totalDamage = damage_party[p];
                                if (strength[p] == true) { totalDamage = totalDamage * 1.5; }

                                combat_hp[targetIndex] = combat_hp[targetIndex] - totalDamage;
                                if (quick_slash[p] == true) { combat_hp[targetIndex] = combat_hp[targetIndex] - totalDamage; }

                                currentMeterGain = 1;
                                if (soul_catcher[p] == true) { currentMeterGain = currentMeterGain + 2; }
                                meter[p] = Math.Min(meter[p] + currentMeterGain, 12);

                                Console.WriteLine("Hit " + enemies[targetIndex] + "! Remaining HP: " + combat_hp[targetIndex]);
                            }
                            else if (choice == "spell")
                            {
                                if (soul_twister[p] == true) { currentMinMeter = 3; }
                                else { currentMinMeter = 4; }

                                if (meter[p] < currentMinMeter)
                                {
                                    Console.WriteLine("Not enough " + resourceNames[p] + "!");
                                }
                                else
                                {
                                    Console.WriteLine(spellPrompts[p]);
                                    string spellChoice = Console.ReadLine().ToLower();

                                    if (spellChoice == "desolate dive" || spellChoice == "thread storm")
                                    {
                                        if (p == 0) { spellDmg = Ghost_spell_manager(damage_party[0], shaman_stone[0], "desolate dive"); }
                                        else { spellDmg = Hornet_spell_manager(damage_party[1], shaman_stone[1], "thread storm"); }

                                        // AOE hits BOTH targets
                                        combat_hp[0] = combat_hp[0] - spellDmg;
                                        combat_hp[1] = combat_hp[1] - spellDmg;
                                        Console.WriteLine("AOE blast hits both bosses!");

                                        if (p == 0) { party_hp[0] = party_hp[0] - (int)(party_hp[0] * 0.33); }
                                    }
                                    else
                                    {
                                        Console.WriteLine("Target 1 or 2?");
                                        choice_attack = Convert.ToInt32(Console.ReadLine());
                                        targetIndex = choice_attack - 1;

                                        if (p == 0) { combat_hp[targetIndex] = combat_hp[targetIndex] - Ghost_spell_manager(damage_party[0], shaman_stone[0], spellChoice); }
                                        else { combat_hp[targetIndex] = combat_hp[targetIndex] - Hornet_spell_manager(damage_party[1], shaman_stone[1], spellChoice); }
                                    }
                                    meter[p] = meter[p] - currentMinMeter;
                                }
                            }
                            else if (choice == "focus" || choice == "heal")
                            {
                                if (meter[p] >= 4)
                                {
                                    int heal = (int)(party_hp[p] * 0.4);
                                    if (quick_focus[p] == true) { heal = heal * 2; }

                                    if (p == 0) { party_hp[0] = Math.Min(125, party_hp[0] + heal); }
                                    else { party_hp[1] = Math.Min(100, party_hp[1] + heal); }

                                    meter[p] = meter[p] - 4;
                                    Console.WriteLine(playerNames[p] + " focused!");
                                }
                            }
                            else
                            {
                                block[p] = true;
                                Console.WriteLine(playerNames[p] + " is ready to parry!");
                            }
                        }

                        // --- 2. WIN CHECK (The Beast is the key!) ---
                        if (combat_hp[0] <= 0)
                        {
                            win = true;
                            Console.WriteLine("\n[!!!] THE BEAST FALLS [!!!]");
                            Console.WriteLine("Seeing her companion explode in a orange mist, the God Tamer discards her weapon.");
                            Console.WriteLine("The crowd roars in approval! You are the champions of the Colosseum!");
                            win_game = true;
                            break;
                        }

                        // --- 3. BOSS TURN ---
                        Console.WriteLine("\n--- The Bosses Counter-Attack! ---");
                        for (int i = 0; i < 2; i++)
                        {
                            if (combat_hp[i] <= 0) 
                            {
                                continue;
                            }

                            // Target selection
                            int target;
                            if (party_hp[0] > 0 && party_hp[1] > 0) { target = roll.Next(0, 2); }
                            else if (party_hp[0] > 0) { target = 0; }
                            else { target = 1; }

                            int finalDmg;
                            if (i == 0) { finalDmg = beastDmg; }
                            else { finalDmg = riderDmg; }

                            // Beast Special: Acid Spit (AOE)
                            if (i == 0)
                            {
                                int spitChance = roll.Next(0, 100);
                                if (spitChance < 30)
                                {
                                    Console.WriteLine("The Beast spits acid across the arena!");
                                    for (int j = 0; j < 2; j++)
                                    {
                                        int acidDmg = 10;
                                        if (block[j] == true) { acidDmg = acidDmg / 2; }
                                        party_hp[j] = party_hp[j] - acidDmg;
                                        Console.WriteLine(playerNames[j] + " takes " + acidDmg + " acid damage!");
                                    }
                                    continue; // Skip the normal attack for the beast this turn
                                }
                            }

                            if (block[target] == true) { finalDmg = finalDmg / 2; }
                            party_hp[target] = party_hp[target] - finalDmg;
                            Console.WriteLine(enemies[i] + " strikes " + playerNames[target] + " for " + finalDmg + "!");
                        }

                        block[0] = false; block[1] = false;
                        if (party_hp[0] <= 0 && party_hp[1] <= 0)
                        {
                            end = true;
                            Console.WriteLine("The heroes were not fools enough to survive the Trial...");
                            break;
                        }
                    }
                }




            }
            if (win_game = true)
            {
                Console.WriteLine("Congrats on winning,Ill add new areas later on,if you wish to remain updated check C0m_Bust3r's Page on istagram");
            }
        }
    }
